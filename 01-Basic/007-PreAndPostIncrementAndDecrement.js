/*
Pre-increment ++i increments the value of i and evaluates to the new incremented value. 

Post-increment i++ increments the value of i and evaluates to the original non-incremented value.
*/

i = 10;
j = 20;
k = 2;

//pre-increment
console.log(`i= ${i} \n++i= ${++i} (first increments and then shows) \n--------------------------------------------`) //first increments and then shows

//post-increment
console.log(`j= ${j} \nj++= ${j++} (firt shows and then increments)    \n--------------------------------------------`) //firt shows and then increments

//pre-decrement
console.log(`i= ${i} \n--i= ${--i} (first decrements and then shows) \n--------------------------------------------`) //first increments and then shows